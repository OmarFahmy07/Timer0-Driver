#ifndef EXIT_REGISTER_H_
#define EXIT_REGISTER_H_

/************************************************************************************/
/* 								REGISTERS' ADDRESSES 								*/
/************************************************************************************/
#define MCUCR 			*((volatile u8*)(0x55))
#define GICR 			*((volatile u8*)(0x5B))
#define MCUCSR 			*((volatile u8*)(0x54))
#define GIFR 			*((volatile u8*)(0x5A))
/************************************************************************************/


/************************************************************************************/
/* 								 SENSE CONTROL 										*/
/************************************************************************************/
#define RISING_EDGE 	3
#define FALLING_EDGE 	2
#define LOGIC_CHANGE 	1
#define LOW_LEVEL 		0
/************************************************************************************/


/************************************************************************************/
/* 							  MCUCR REGISTER BITS									*/
/************************************************************************************/
#define ISC11 			3
#define ISC10 			2
#define ISC01 			1
#define ISC00 			0
/************************************************************************************/


/************************************************************************************/
/* 							  MCUCSR REGISTER BITS									*/
/************************************************************************************/
#define ISC2			6
/************************************************************************************/


/************************************************************************************/
/* 							  GICR REGISTER BITS									*/
/************************************************************************************/
#define INT1			7
#define INT0 			6
#define INT2			5
/************************************************************************************/


/************************************************************************************/
/* 							  GIFR REGISTER BITS									*/
/************************************************************************************/
#define INTF1			7
#define INTF0			6
#define INTF2			5
/************************************************************************************/


/************************************************************************************/
/* 						   ISRs PROTOTYPES - ATTRIBUTES 							*/
/************************************************************************************/
void __vector_1 (void) __attribute__ ((signal));
void __vector_2 (void) __attribute__ ((signal));
void __vector_3 (void) __attribute__ ((signal));
/************************************************************************************/


/************************************************************************************/
/* 						   EXTERNAL INTERRUPTS DEFINITIONS 							*/
/************************************************************************************/
#define EXTERNAL_INTERRUPT0				0
#define EXTERNAL_INTERRUPT1				1
#define EXTERNAL_INTERRUPT2				2
#define NUM_OF_EX_INTERRUPTS			3
/************************************************************************************/


#endif /* EXIT_REGISTER_H_ */
